    private void beställningar(){
        
        try
        {
            String beställningar = "Select BestallningsID, Datum, Namn from Bestallning join Personal P on P.PersonalID = Bestallning.Personal";
            ArrayList<HashMap<String, String>> allaBeställningar = idb.fetchRows(beställningar); 
            
            for (HashMap<String, String> hatt : allaBeställningar){
                
                Object[] hattData = {
                    hatt.get("BestallningsID"),
                    hatt.get("Datum"),
                    hatt.get("Namn"),                    
                };
                model.addRow(hattData);

            }

            
        }catch (InfException ettUndantag) {
            
            JOptionPane.showMessageDialog(null, "Databasfel!");
            System.out.println("Internt felmeddelande" + ettUndantag.getMessage());
            
        }
        
        catch (Exception ettUndantag) {
            
            JOptionPane.showMessageDialog(null, "Något gick fel!");
            System.out.println("Internt felmeddelande" + ettUndantag.getMessage());
            
        }
        
        
    }
    

